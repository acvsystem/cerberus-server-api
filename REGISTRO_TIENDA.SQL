    NRO_DOCUMENTO_EMPLEADO VARCHAR(10),
    HR_EXTRA_ACUMULADO VARCHAR(5),
    HR_EXTRA_SOLICITADO  VARCHAR(5),
    HR_EXTRA_SOBRANTE  VARCHAR(5),
    ESTADO VARCHAR(20),--CORRECTO/APROBAR/UTILIZADO/
    APROBADO BOOLEAN,
    SELECCIONADO BOOLEAN,
    FECHA VARCHAR(11),
    FECHA_MODIFICACION  VARCHAR(11),
    PRIMARY KEY (ID_HR_EXTRA)
);

CREATE TABLE TB_HEAD_PAPELETA(
    ID_HEAD_PAPELETA INT AUTO_INCREMENT,
    CODIGO_PAPELETA VARCHAR(11),
    NOMBRE_COMPLETO VARCHAR(100),
    NRO_DOCUMENTO_EMPLEADO VARCHAR(10),
    ID_PAP_TIPO_PAPELETA INT,
    CARGO_EMPLEADO VARCHAR(50),
    FECHA_DESDE VARCHAR(11),
    FECHA_HASTA VARCHAR(11),
    HORA_SALIDA VARCHAR(5),
    HORA_LLEGADA VARCHAR(5),
    HORA_ACUMULADA VARCHAR(5),
    HORA_SOLICITADA VARCHAR(5),
    CODIGO_TIENDA VARCHAR(3),
    FECHA_CREACION VARCHAR(11),
    DESCRIPCION VARCHAR(1000),
    PRIMARY KEY(ID_HEAD_PAPELETA),
    FOREIGN KEY (ID_PAP_TIPO_PAPELETA) REFERENCES TB_TIPO_PAPELETA(ID_TIPO_PAPELETA)
);

CREATE TABLE TB_DETALLE_PAPELETA(
    ID_DETALLE_PAPELETA INT AUTO_INCREMENT,
    DET_ID_HEAD_PAPELETA INT,
    DET_ID_HR_EXTRA INT,
    HR_EXTRA_ACUMULADO VARCHAR(5),
    HR_EXTRA_SOLICITADO  VARCHAR(5),
    HR_EXTRA_SOBRANTE  VARCHAR(5),
    ESTADO VARCHAR(20),--CORRECTO/APROBAR/UTILIZADO/
    APROBADO BOOLEAN,
    SELECCIONADO BOOLEAN,
    FECHA VARCHAR(11),
    FECHA_MODIFICACION  VARCHAR(11),
    PRIMARY KEY(ID_DETALLE_PAPELETA),
    FOREIGN KEY (DET_ID_HEAD_PAPELETA) REFERENCES TB_HEAD_PAPELETA(ID_HEAD_PAPELETA),
    FOREIGN KEY (DET_ID_HR_EXTRA) REFERENCES TB_HORA_EXTRA_EMPLEADO(ID_HR_EXTRA)
);

CREATE TABLE TB_CONFIGURACION_TOLERANCIA_HORA(
    ID_TOLERANCIA INT AUTO_INCREMENT,
    REFERENCIATIEMPO_TOLERANCIA VARCHAR(50),
     VARCHAR(10),
    PRIMARY KEY(ID_TOLERANCIA)
);